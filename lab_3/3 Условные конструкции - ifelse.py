# Задача 1: Проверка четности числа
num = int(input("Введите число: "))
if num % 2 == 0:
    print("Четное")
else:
    print("Нечетное")

# Задача 2: Наибольшее из двух чисел
a = float(input("Введите первое число: "))
b = float(input("Введите второе число: "))
print(max(a, b))

# Задача 3: Проверка возраста для водительских прав
age = int(input("Введите ваш возраст: "))
if age >= 18:
    print("Можно получить водительские права")
else:
    print("Нельзя получить водительские права")